// Dengan menambahkan Else-If, kita dituntut untuk membuat percabangan lebih kompleks
var nilai= prompt ('Masukkan nilai:');
// Kondisi
if (nilai % 2 ==0) {
	// Aksi1
	alert (nilai+ ' adalah bilangan POSITIF');
} else if (nilai % 2 ==1){
	// Aksi2
	alert (nilai+ ' adalah bilangan NEGATF');
} else {
	alert (nilai+ ' Bukan angka');
}